function isEven (number){
    if(number % 2 ===0){
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(5));
console.log(isEven(100))

function isodd (number){
    if(number % 2 ===1){
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(7));
console.log(isEven(50))